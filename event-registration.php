<h2>Entitlements</h2>
<h3>Full Conference Registration</h3>
<p>Full registration entitlements include:</p>
<ul>
    <li>Unique attendee password-protected access to the AMTA 2020 virtual conference platform</li>
    <li>All program content delivered live</li>
    <li>Ability to engage with presenters and other attendees during live presentations through a discussion channel & live Q&A</li>
    <li>Ability to take notes during sessions and export to email</li>
    <li>Access to view all presentations on-demand, post live presentation</li>
    <li>Access to view all presentation abstracts and speaker information</li>
    <li>Access to view the AMTA PechaKucha Gallery</li>
    <li>Access to view the AMTA virtual exhibition and opportunity to engage with key sponsors and exhibitors</li>
    <li>Opportunity to meet other attendees, exchange contact details and set meetings via the AMTA 2020 meeting hub</li>
    <li>Approx. 18.25 CPD hours</li>
    <li>Certificate of CPD</li>
</ul>
<h3>Session Registration</h3>
<p>1 or 3 session registration entitlements include:</p>
<ul>
    <li>Unique attendee password-protected access to the AMTA 2020 virtual conference platform</li>
    <li>Selected session content delivered live</li>
    <li>Ability to engage with presenters and other attendees during selected presentations through a discussion channel & live Q&A</li>
    <li>Ability to take notes during selected sessions and export to email</li>
    <li>Access to view selected session presentations on-demand, post live presentation</li>
    <li>Access to view selected session presentation abstracts and speaker information</li>
    <li>Access to view the AMTA PechaKucha Gallery</li>
    <li>Access to view the AMTA virtual exhibition and opportunity to engage with key sponsors and exhibitors</li>
    <li>Opportunity to meet other attendees, exchange contact details and set meetings via the AMTA 2020 meeting hub</li>
    <li>Between 1 – 10.5 CPD hours depending on session amount and session length chosen.</li>
    <li>Certificate of CPD</li>
</ul>
<h3>PDS Registration</h3>
<p>PDS registration entitlements include:</p>
<ul>
    <li>Unique attendee password-protected access to the AMTA 2020 virtual conference platform</li>
    <li>All PDS program content delivered live</li>
    <li>Ability to engage with PDS presenters and other attendees during live presentations through a discussion channel & live Q&A</li>
    <li>Ability to take notes during PDS sessions and export to email</li>
    <li>Access to view all PDS presentations on-demand, post live presentation</li>
    <li>Access to view all PDS presentation abstracts and speaker information</li>
    <li>Approx. 6 CPD hours</li>
    <li>Certificate of CPD</li>
</ul>
