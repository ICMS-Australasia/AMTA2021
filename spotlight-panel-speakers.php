<h2>The Evolving Symphonies of Life: Conducting Music Therapy in Cancer and Palliative Care across the Lifespan</h2>

<p>Being diagnosed with a life-threatening illness is life-changing for the individual and their family. Music therapy can do much in this space, integrating moments of peace and comfort into treatment and care, amplifying voices of stability and familiarity, strengthening bonds and connection, and transforming experiences of fear and sadness into moments of joy and fun. The Panel will explore how music therapy can amplify and evolve the resilience and strengths of the patient and family, support wellbeing and living fully, facilitate moments of sharing and support, and provide enduring legacies.</p>

<div class="row mb-5">
    <div class="col">
        <img src="./img/speakers/schreck.png" class="img-fluid float-right ml-4" width="200">
        <h3 class="mb-2 mt-0"><a href="#Schreck" data-toggle="modal">Brian Schreck</a></h3>
        <p class="my-0"><b>Amplified Cardio-Pulmonary Recordings (ACPR) —A Music Therapy Legacy Intervention with Adult Oncology Patients and their Families</b></p>
        <p class="mt-4">Brian will discuss using Amplified Cardio-Pulmonary Recording with Adult Oncology Patients. The intervention consists of recording the patient’s heart/lungs, then combining it with either original or pre-recorded music that has special meaning for the patient and their family. This intervention technique results in a recording that the patient and family can use to maintain connection. Amplified Cardio-Pulmonary Recordings (ACPR) accentuate the potential for life rhythms and meaningful melodies that highlight direct or subtle aspects of a lived experience through preservation of vital sounds and live and recorded music.</p>
    </div>
</div>
