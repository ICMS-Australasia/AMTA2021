<h2>Voices of South-East Asian Music Therapy</h2>

<p>Throughout history, Southeast Asia has been a melting pot of cultures, religions, food heritages, along with spiritual, healing, and music traditions. The few qualified SEA music therapists face unique opportunities and challenges as they juggle numerous professional plates. This special platform will bring together unique perspectives of how music therapists are bridging larger intersectional themes of healing, amplifying indigenous music roots, and advocating for the profession within their own east-west situatedness in clinical practice, across the intersections of Malaysia, Thailand, Indonesia, and Singapore.</p>

