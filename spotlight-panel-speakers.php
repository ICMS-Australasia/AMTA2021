        <p class="my-0"><b>“Adding life to a child’s years…”: The role of music therapy in promoting life, growth and joy for a paediatric palliative care patient and family.</b></p>
        <p class="mt-4">The Paediatric Palliative Care (PPC) service is often viewed with fear and apprehension given the common misconception that a referral to PPC signifies ‘giving up’ on a child and their treatment. Instead, PPC clinicians work alongside treating medical teams to enhance the quality of life of the child with a life-limiting condition and their family. Music therapy plays an important role in providing holistic care that promotes well-being, and helps a child to live their best life. This presentation will chronicle the journey of a PPC patient and family thus far, demonstrating how music therapy, in conjunction with other PPC allied health disciplines, continues to provide opportunities for growth, self-expression, and enjoyment, even during a pandemic.</p>
