            <h3>Brief description:</h3>

            <p>Perspectives sessions are an opportunity for <em>two presenters with some sort of contrasting perspective on a given topic</em> to connect and discuss their ideas and experience. Those wishing to submit a Perspectives session should be prepared to articulate the topic of their session, the two presenters who will be speaking and their point of difference, and how they will approach constructive debate.</p>

            <h3>Detailed description:</h3>

            <p>Perspectives sessions are a new opportunity to promote rich discussion and constructive debate on a particular topic. The conversation should be centred on a specific topic, such as a population, music therapy approach, clinical context, or other issue impacting the music therapy discipline and community. The purpose of Perspectives sessions is to bring the music therapy community together by fostering rich dialogue and examining the differences that exist within the profession. The two speakers should each prepare a short presentation from their perspective (approximately 10 minutes), after which the speakers will dialogue about their differences. A feature of the Perspectives sessions is that attendees are invited to submit questions beforehand, which the speakers will then speak to during the session.</p>
            <p>In submitting a Perspectives session, speakers should ensure they have an agreed upon topic, and a clear difference in experience or perspective between the two speakers. Some examples of differing perspectives:</p>

            <ul>
              <li>A music therapist and a music therapy participant exploring a particular context (as an employee versus someone with lived experience).</li>
              <li>A therapist and a teacher both working with children in a school context.</li>
              <li>A music therapist and another therapist in an aged care context.</li>
              <li>Two clinicians who work with children with Autism Spectrum Disorder, one from a family centred perspective, and one from a developmental perspective.</li>
              <li>Two clinicians who work with adults in mental health, one who primarily uses improvisation, one who primarily uses song writing.</li>
              <li>Two researchers who explore the impacts of music therapy, one using quantitative research methods, one who uses qualitative research methods.</li>
              <li>Two RMTs who are passionate advocates for music therapy with a clear difference in how they believe we can strengthen the profession.</li>
              <li>Two music therapists who work in a particular context, one who is a new graduate, one who has been practicing for decades.</li>
            </ul>

