<h2>Submission criteria and review process</h2>

<p>Each abstract will be peer reviewed by two scientific committee members and ranked according to the following selection criteria:</p>

<ul>
  <li>Adherence to criteria listed in the submission template</li>
  <li>Relevance to the conference theme</li>
  <li>Contribution of new knowledge/understanding to the music therapy profession</li>
  <li>Clarity and coherence of written expression</li>
</ul>

<h2>Successful applicant terms & conditions</h2>

<p>Successful applicants will be offered a place in the conference program. Where possible, successful applicants will be offered their first preference of presentation format at the conference. However, some may be invited to present in a different format.</p>

<p>All presenters will be required to formally accept the invitation by registering and paying the conference registration fee.</p>

