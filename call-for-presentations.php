            <h3>Brief description:</h3>

            <p>PechaKucha is a presentation methodology for speakers who have conducted research or evaluation to share their work in a format of <em>20 slides for 20 seconds each</em> (6 minutes and 40 seconds). PechaKucha is designed to be concise and focused on the ‘story’ of the research. PechaKucha sessions are perfectly suited to researchers, graduate researchers, minor thesis presentations, student/new grads, and evaluation/quality improvement projects. All PechaKucha presentations will be allocated into a session relevant to their research.</p>

            <h3>Detailed description:</h3>

            <p>PechaKucha sessions are designed for presenters who wish to share research findings in an engaging and concise format. The PechaKucha style of presentation translates to “chit chat” in Japanese, and the purpose is to “talk less, show more”, strictly limiting the format to 20 slides for 20 seconds each. Presenters must utilise visual imagery for their presentations and should consider the <em>most efficient way to verbally deliver their research in the 6.40 minutes timing</em>. PechaKucha sessions are perfectly suited to researchers, graduate researchers, and minor thesis presentations, as well as evaluation projects in clinical contexts. Each presenter will then be allocated into a session of other presenters. PechaKucha sessions will have a technical assistant to provide support with preparing speakers, moderating the chat thread, and moderating the Q&A session at the end. You can learn more about PechaKucha <a href="https://www.youtube.com/watch?v=L31SwpN1dAc" target="_blank">here</a>.</p>


