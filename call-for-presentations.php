<h2>Abstract Format</h2>
<p>Abstracts should address the conference theme, must not exceed 400 words and must not contain tables or graphs. To ensure a blind review, please ensure your abstract does not contain identifying material. This includes reference to unique features about the setting, context, client or author, or the city in which the work/study has been undertaken.</p>
<h3>Biography</h3>
<p>Please include a brief biography, no more than 50 words.</p>

<h2>Submissions</h2>
<p>Submissions are to be made online via the link at the top of the page.</p>


<h3>Submission criteria and review process</h3>

<p>Each abstract will be peer reviewed by two scientific committee members and ranked according to the following selection criteria:</p>

<ul>
  <li>Relevance to the conference theme</li>
  <li>Contribution of new knowledge/understanding to the music therapy profession</li>
  <li>Clarity and coherence of written expression</li>
</ul>

