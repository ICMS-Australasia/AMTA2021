<h2>Conference theme</h2>

<h3>‘Connection: Evolving, reflecting, responding’</h3>

<p>In the face of crisis, whether on a personal, community, or systemic level, music has the capacity to forge and sustain connection. As music therapists, we are leaders in understanding the affordances of and relationship between music and human connection. Every day across clinical practice, advocacy, and research contexts, RMTs carefully consider and harness music as a tool to address the multiple and wide-ranging needs for connection that exist in our world.</p>
<p>Connection is a response to isolation; it is the antithesis of barriers. As a discipline, music therapy is built on the concept of connection. Across every context, approach, or therapeutic orientation, connection is fundamental. This year’s theme lies at the heart of music therapy as a strengths-based profession: the capacity for connection is a resource we all possess and can continually build on, not only as individuals, but within and between communities.</p>
<p>When the conference committee decided upon this theme at the end of 2019, little did we know that it would become even more relevant, significant, and urgently important in 2020. In responding to the needs for alternative ways to connect our profession through an on-line conference this year, we have reimagined the program and opened up several new modes of presentation. We encourage you to take some time to examine each of these new categories, reflect on what you can bring as a presenter, and how you can contribute to this year’s on-line conference.</p>
<p>Whether you are highlighting the role of music therapy in connecting functional gains to socio-emotional goals; developing innovative tele-health practice to connect with clients in new and emerging ways; responding to the need for greater connection and collaboration in multi/trans-disciplinary teams; or passionate about highlighting the connection between structural inequalities and what are perceived to be our clients’ ‘personal problems’  – we invite you to consider your practice through the lens of this year’s theme, and how your presentation can be best brought to life across the different submission categories.</p>

<h2>Presentation Categories</h2>

<p>With the transition on-line, the AMTA 2020 Conference program will look a little different, featuring <strong><em>six new presentation categories</em></strong>.</p>

<p>These categories speak to the need for a rich and dynamic program that can bring our community together virtually. Further, this year’s on-line program will allow us to harness the opportunity to open our national conference up to international speakers and delegates.</p>

<p>In addition to regular paper presentations, we invite you to peruse the diverse suite of presentation categories open for your submission:</p>

<ul>
  <li>Masterclass</li>
  <li>Panel</li>
  <li>Perspectives session</li>
  <li>Paper or virtual roundtable</li>
  <li>PechaKucha</li>
</ul>

