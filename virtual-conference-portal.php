<p>We will be using the online platform 'OnAir' for the virtual conference, which is a cloud-based secure streaming conference solution.  So, there is nothing to download!</p>
<p>In the virtual platform, interaction is the key! You will be able to view the full program and save your favourites to customise your program. You can watch live sessions and pre-recorded presentations (including post questions and communicate in Q&A), you can visit the Virtual Exhibition and view and access content in the virtual booths, as well as connect and meet with exhibitors, PLUS make connection with delegates via our networking sessions and the Meeting Hub.</p>
<p>Below is a quick guide to get you started, but more in-depth step by step instructions will be sent with your log-in details.</p>

<h3>Logging In</h3>
<p>A link to the platform along with an individual log-in code will be sent to you prior to the conference, to access the portal. This will give you time to familiarise yourself with the portal and review the program, virtual exhibition and meeting hub. You can also save your favourites for a customised program!</p>

<h3>What is needed to watch the virtual meeting?</h3>
<p>All that is needed for attendees is:</p>
<ul>
  <li>A computer, laptop or tablet</li>
  <li>A stable internet connection (we suggest closing any unused browsers/programs that could be draining on your internet bandwidth)</li>
  <li>Audio & mic (computer speakers and microphone)</li>
  <li>Webcam (not mandatory for attendees but handy if you wish to participate in the networking sessions, meeting hub and exhibitor meetings)</li>
</ul>

<h3>Which browser should we use for the virtual conference?</h3>
<p>Most browsers can be used (latest versions) - these include Google Chrome, Microsoft Edge, Firefox, iPad Safari.</p>
<p>Note if you have not connected to a virtual event before, your browser may not be correctly set for your audio and video settings. Please check these are set to 'allow' connection.  Also when first logging in to the conference portal it should prompt you to enable the webcam and microphone.</p>
<p>Please note <u>Internet Explorer is NOT recommended</u> and not a supported browser.</p>

<h3>Can more than one person use the conference login details?</h3>
<p>No. Each registration is just for a single person, and your unique login is for your personal use only (this includes your personal profile). Content cannot be exported out of the platform for a shared experience.</p>

<h3>How do I setup my profile?</h3>
<p>Upon logging in to the virtual platform, your Profile/Account tab will appear - we encourage you to add a photo, short biography, and links to your social media accounts.  You can edit your profile at any time.</p>
<p>Your profile tab is also where you can restrict/allow what is seen in the Meeting Hub/online delegate list.</p>

<h3>What days/times are the sessions?</h3>
<p>The AMTA 2020 conference commences with the Professional Development Seminar on Saturday 7 November 2020. The Conference runs from Sunday 8 November â€“ Sunday 15 November 2020.</p>
<p>The displayed times will automatically adjust to the time zone you selected in your profile set up.</p>
<p>Please view the <a href="./full-program">full program here</a> for times and session details.</p>
<p><em>Please note that the times on the program are in Australian Eastern Daylight Time (AEDT) which is Victoria, New South Wales, Tasmania and Australian Capital Territory time. If you are in QLD, WA or the NT, please make note of the time difference.</em></p>

<h3>When will I receive the instructions for attending the virtual event?</h3>
<p>We are working diligently to prepare for this unique experience and will send you the necessary instructions a few days prior to the event. Make sure that the email address associated with your registration is accurate and up to date so that there is no delay in receiving our communications.</p>

<h3>Will sessions be pre-recorded or live?</h3>
<p>The presentations will be a mixture of pre-recorded presentations and live presentations, and interaction with the presenter will be available via live Q&A. All conference sessions will be recorded and will be available to watch on-demand post conference. Please note that the PDS sessions will not be recorded or available on-demand.</p>

<h3>Will sessions be recorded and available after the conference?</h3>
<p>Yes, for registered participants only, the conference presentations will be made available within 2 hours of the live session ending. These recordings will be available for 3 months after the conference. This also includes access to the virtual exhibition, meeting hub, and all handouts. Please not that closed captions will be added to live videos after the conference.</p>

<h3>Will attendees be able to ask questions during sessions?</h3>
<p>Yes, via the Q&A box in the session as well as communicate/discuss with other delegates in the Discussion Forum.</p>

<h3>What is the Meeting Hub?</h3>
<p>The Meeting Hub allows you to connect and communicate with other delegates. You can search for delegates, and connect with them by clicking on the Connect button. Once the other delegate accepts your request, you can choose to interact with your connection by starting a live chat or live video call. You can also schedule to meet at a future time, send messages and take notes. Contact information for all attendees you have connected with is included when you export your contacts.</p>
<p>When you first enter the conference portal delegates will be asked to update their profile, and this includes 'privacy' and 'meeting hub' options where you can choose what contact information you wish to share.</p>

