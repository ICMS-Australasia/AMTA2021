<p>The generous benefits of sponsorship offer exposure to over 250 health professionals via the virtual conference platform and online via the website in the lead up to the event. The limited Platinum, Gold and Silver sponsorship packages will provide supporters exclusive benefits that will showcase their business or organisation, and will communicate their dedicated support of music therapy. These packages will allow supporters to be part of the virtual exhibition which offers exposure through branding, engagement and networking with 2020 attendees. The virtual exhibition will be accessible by attendees not only during the conference dates, but also for a period after the conference, therefore supporter exposure is maximised. Supporters have the opportunity to connect with attendees and swap contact details as well as receive analytics regarding attendee traffic and engagement.</p>
<p>Conference sponsorship provides a unique opportunity to target your marketing to those who really need to see it. If an individual, a business, or an organisation you know of could benefit from partnering with AMTA this year, please forward them this information.</p>
